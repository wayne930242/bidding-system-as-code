package twooverone

import com.github.phisgr.bridge.BiddingTree

/**
 * 1C 開叫及應叫
 * C至少3張，12-21 HCP
 */
fun BiddingTree.oneClub() {
    explanation = "C至少3張（3-3低花時開叫1C），12-21點"

    // 一副高花應叫
    "1D" - "4+D無4張高花，或5+D有開叫實力準備叫出高花" {
        oneDiamondResponseAfterOneClub()
    }

    "1H" - "4+H，5+點" {
        oneMajorResponseAfterMinor()
    }

    "1S" - "4+S，5+點" {
        oneMajorResponseAfterMinor()
    }

    // 無將應叫
    "1N" - "8-10點，平均牌型，無4張高花"
    "2N" - "12-15點，平均牌型，無4張高花"
    "3N" - "15-18點，平均牌型，無4張高花"

    // 反常低花加叫
    "2C" - "反常加叫：9+點，4+C支持，迫叫一輪，無4張高花" {
        invertedMinorRaise()
    }

    "3C" - "阻擊性加叫：5-9點，5+C支持，無4張高花" {
        "Pass" - "低限，不進局"
        "3N" - "19-20點平均牌型"
    }

    // 朗·Feldman2D應叫
    "2D" - "Feldman約定：低花好牌滿貫意圖（6+D強牌組或5-5雙低花）" {
        feldman2D()
    }

    // 阻擊性跳叫新花色
    "2H" - "阻擊：6張H牌組，3-6點"
    "2S" - "阻擊：6張S牌組，3-6點"
    "3D" - "阻擊：7張D牌組"
    "3H" - "阻擊：7張H牌組"
    "3S" - "阻擊：7張S牌組"

    // 二蓋一應叫（進局迫叫）
    // 注意：1C後2C是反常加叫，不是二蓋一
}


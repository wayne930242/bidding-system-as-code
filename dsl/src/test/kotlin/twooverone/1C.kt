/**
 * 低花開叫後一副高花應叫的開叫者再叫
 */
private fun BiddingTree.oneMajorResponseAfterMinor() {
    "1S" - "4+S（如應叫為1H）"
    "1N" - "12-15點平均牌型，無4張應叫花色支持"

    "2C" - "6+C，12-15點" {
        "2D" - "第四花色牌組迫叫"
    }

    // 簡單加叫（低限）
    "2H" - "4張H支持，12-15點（如應叫為1H）"
    "2S" - "4張S支持，12-15點（如應叫為1S）"

    "2N" - "19-20點平均牌型，無4張應叫花色支持，另一高花可能4張"

    // 跳加叫（邀叫性）
    "3H" - "4張H支持，非平均牌型，15-18點（如應叫為1H）"
    "3S" - "4張S支持，非平均牌型，15-18點（如應叫為1S）"

    // Splinter跳叫（高限可成局）
    "3D" - "Splinter跳叫（跳倒叫）：4張應叫花色支持，D短牌組，19+點"
    // 1C-1H-3D = 4H支持，D短牌組
    // 1C-1S-3D = 4S支持，D短牌組
    // 1C-1S-3H = 4S支持，H短牌組

    // 直接進局（高限平均牌型）
    "4H" - "4張H支持，平均牌型，19+點（如應叫為1H）"
    "4S" - "4張S支持，平均牌型，19+點（如應叫為1S）"

    "3N" - "16-18點，6-7張接近堅實C牌組，應叫花色單張或雙張小"

    // Walsh割裂叫牌
    "4C" - "Walsh割裂：6+C，4張應叫花色支持，滿貫意圖"
    "4D" - "Walsh割裂：6+C，4張應叫花色支持，成局力量無滿貫意圖"
}

